# Color scale for pollution levels
def get_pollution_color(aqi):
    if aqi <= 50:
        return '#00E400'  # Good
    elif aqi <= 100:
        return '#FFFF00'  # Moderate
    elif aqi <= 200:
        return '#FF7E00'  # Unhealthy
    elif aqi <= 300:
        return '#FF0000'  # Very Unhealthy
    elif aqi <= 400:
        return '#99004C'  # Hazardous
    else:
        return '#7E0023'  # Very Hazardous 